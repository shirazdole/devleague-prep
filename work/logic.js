function isNumberGreaterThan(first,second){
	if (first>second){
		return true;
	}
	else{
		return false;
	}
}
console.log(isNumberGreaterThan(10,15));

function isTrue(val){
	if (val === 1){
		return true;
	}
	else{
		return false;
	}
}
console.log(isTrue(1));

function isFalse(val){
	if (val === 1){
		return false;
	}
	else{
		return true;
	}
}
console.log(isFalse(1));

